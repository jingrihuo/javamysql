package cn.zucc.edu.summerWork.model;

public class Beancarcategory {
	private String categorynum;//车系编号
	private String brandnum;//品牌编号
	private String categoryname;//车系名称
	private String categorytype;//车系添加
	public String getCategorynum() {
		return categorynum;
	}
	public void setCategorynum(String categorynum) {
		this.categorynum = categorynum;
	}
	public String getBrandnum() {
		return brandnum;
	}
	public void setBrandnum(String brandnum) {
		this.brandnum = brandnum;
	}
	public String getCategoryname() {
		return categoryname;
	}
	public void setCategoryname(String categoryname) {
		this.categoryname = categoryname;
	}
	public String  getCategorytype() {
		return categorytype;
	}
	public void setCategorytype(String categorytype) {
		this.categorytype = categorytype;
	}
}
