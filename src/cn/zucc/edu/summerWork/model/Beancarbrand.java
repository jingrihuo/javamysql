package cn.zucc.edu.summerWork.model;

public class Beancarbrand {
	private String brandnum;//品牌编号
	private String brandname;//品牌名称
	private String brandcountry;//品牌国别
	private String brandintroduce;//品牌介绍
	private String brandtype;//品牌添加
	public String getBrandnum() {
		return brandnum;
	}
	public void setBrandnum(String brandnum) {
		this.brandnum = brandnum;
	}
	public String getBrandname() {
		return brandname;
	}
	public void setBrandname(String brandname) {
		this.brandname = brandname;
	}
	public String getBrandcountry() {
		return brandcountry;
	}
	public void setBrandcountry(String brandcountry) {
		this.brandcountry = brandcountry;
	}
	public String getBrandintroduce() {
		return brandintroduce;
	}
	public void setBrandintroduce(String brandintroduce) {
		this.brandintroduce = brandintroduce;
	}
	public String  getBrandtype() {
		return brandtype;
	}
	public void setBrandtype(String brandtype) {
		this.brandtype = brandtype;
	}
}
